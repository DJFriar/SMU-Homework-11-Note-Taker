var express = require("express");

// ==============================================================================
// CONFIGURATION
// ==============================================================================
var app = express();
var PORT = process.env.PORT || 8080;
app.use(express.urlencoded({ extended: true }));
app.use(express.json());

// ================================================================================
// Routes
// ================================================================================
require("./routes")(app);

// =============================================================================
// LISTENER
// =============================================================================
app.listen(PORT, function() {
  console.log("App listening on PORT: " + PORT);
});
